      data: {'orderId': orderId, 'status': 'shipped', 'tracking': trackingNumber},
      createdAt: DateTime.now(),
    );

    await _notificationService.sendNotification(notification);
  }

  /// Send notification when order is delivered
  static Future<void> sendOrderDeliveredNotification(
    String userId,
    String orderId,
  ) async {
    final notification = NotificationModel(
      id: '',
      userId: userId,
      title: 'Order Delivered! ✅',
      body: 'Your order #$orderId has been delivered. Hope you enjoy your purchase!',
      type: NotificationType.order,
      data: {'orderId': orderId, 'status': 'delivered'},
